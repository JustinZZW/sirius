package de.unijena.bioinf.babelms.mzxml.model;

import java.io.Serializable;

public class NameValuePair implements Serializable, DefinitionListHelper.Applicable {

    private static final long serialVersionUID = -2740068644079047524L;

    private String name;
    private String value;
    private String type;

    public NameValuePair() {
        this(null, null, null);
    }

    public NameValuePair(String name, String value, String type) {
        this.name = name;
        this.value = value;
        this.type = type;
    }

    public DefinitionListHelper buildDefinitionList(DefinitionListHelper helper) {
        return helper.def(name, value);
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        NameValuePair that = (NameValuePair) o;

        if (name != null ? !name.equals(that.name) : that.name != null) return false;
        if (type != null ? !type.equals(that.type) : that.type != null) return false;
        if (value != null ? !value.equals(that.value) : that.value != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = name != null ? name.hashCode() : 0;
        result = 31 * result + (value != null ? value.hashCode() : 0);
        result = 31 * result + (type != null ? type.hashCode() : 0);
        return result;
    }
}
